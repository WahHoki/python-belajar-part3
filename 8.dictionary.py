# list -> array, dimana kita mengakses data dengan mengguanakan index

data_list = ['ucup','otong','dadang']

print(data_list[0])

# dictionary (dict) -> associative array
# identifier -> key

data_dict = {
    'key':'value',
    'cp':'ucup',
    'tg':'otong',
    'dg':'dadang',
    'nbr':100,
    'list':data_list,
}
print(data_dict['tg'])
print(data_dict['nbr'])
print(data_dict['list'])
